    private void llenarListaFacultades(){
        DefaultListModel model = new DefaultListModel();
        
        int index = 0;
        for(Facultad fac: universidadController.getListaFacultades()){
            String data = fac.getNombre() + " " + fac.getTelefono();
            model.add(index, data);
            index++;
        }
        lstFacultades.setModel(model);
    }
    

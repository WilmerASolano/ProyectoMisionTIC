    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        Facultad facul = new Facultad(txtFacultad.getText(), txtTelefono.getText());
        this.universidadController.crearFacultad(facul);
        
        this.llenarListaFacultades();
        this.limpiarCajasTextoAdministracion();
    }//GEN-LAST:event_btnCrearActionPerformed

    private void limpiarCajasTextoAdministracion(){
        txtFacultad.setText("");
        txtTelefono.setText("");
    }
    
    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String nombreFacultad = txtFacultad.getText().trim();
        this.limpiarCajasTextoAdministracion();
        Facultad fac = this.universidadController.existeFacultad(nombreFacultad);
        if (fac != null){
             this.txtFacultad.setText(fac.getNombre());
             this.txtTelefono.setText(fac.getTelefono());
        }else{
            JOptionPane.showMessageDialog(this, "No se encontro la facultad: " + nombreFacultad);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        String nombreFacultad = txtFacultad.getText();
        boolean esFacultad = false;
        for(Facultad fac: Principal.uniMintic.getFacultades()){
            if (fac.getNombre().equals(nombreFacultad)){
                fac.setTelefono(txtTelefono.getText());
                JOptionPane.showMessageDialog(this, "Facultad Actualizada correctamente");
                esFacultad = true;
                break;
            }
        }
        if (!esFacultad)
            JOptionPane.showMessageDialog(this, "No se encuentra la facultad");
        else{
            this.llenarListaFacultades();
        }
        this.limpiarCajasTextoAdministracion();
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        String nombreFacultad = txtFacultad.getText();
        boolean seElimino = this.universidadController.eliminarFacultad(nombreFacultad);
        if (seElimino == false)
            JOptionPane.showMessageDialog(this, "No se encontr贸 la facultad: " + nombreFacultad);
        else{
            JOptionPane.showMessageDialog(this, "Se elimn贸 la Facultad: " + nombreFacultad);
            this.llenarListaFacultades();
            this.limpiarCajasTextoAdministracion();
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnAsignarProfesorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAsignarProfesorActionPerformed
        Profesor profe = new Profesor(txtProfesor.getText(), txtTelefonoProfe.getText(), txtDocumentoProfesor.getText());
        String nombreFacultad = cmbFacultadProfesor.getSelectedItem().toString();
        //JOptionPane.showMessageDialog(this, nombreFacultad);
        FacultadController facController = new FacultadController(nombreFacultad);
        facController.asignarProfesor(profe);
    }//GEN-LAST:event_btnAsignarProfesorActionPerformed


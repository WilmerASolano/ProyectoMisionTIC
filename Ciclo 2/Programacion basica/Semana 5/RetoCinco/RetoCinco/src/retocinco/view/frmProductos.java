    public void llenarComboProveedor(){
        int index =0;
        for (int i = 0; i < productocontroller.listaProveedor().size(); i++) {
           listaProveedor[i][0] = productocontroller.listaProveedor().get(i).getId_proveedor() + "";
           String data = productocontroller.listaProveedor().get(i).getId_proveedor() + " " + productocontroller.listaProveedor().get(i).getRazon_social() + " " + productocontroller.listaProveedor().get(i).getNIT() + " " + productocontroller.listaProveedor().get(i).getTelefono() + " " + productocontroller.listaProveedor().get(i).getId_direccion() + " " + productocontroller.listaProveedor().get(i).getCorreo() + " " + productocontroller.listaProveedor().get(i).getRepresentante() + " " + productocontroller.listaProveedor().get(i).getSitio_web();
           listaProveedor[i][1]= data;
           this.cbProveedor.addItem(data);
           index++;
        }
        
    }
    public void llenarComboExistencia(){
        int index=0;
        for (int i = 0; i < productocontroller.listaExistencia().size(); i++) {
            listaExistencia[i][0] = productocontroller.listaExistencia().get(i).getId_existencia() + "";
            String data = productocontroller.listaExistencia().get(i).getId_existencia() + " " + productocontroller.listaExistencia().get(i).getNombre();
            listaExistencia[i][1] = data;
            this.cmbExistencia.addItem(data);
            index++;
        }
        
    }
    public void llenarComboCategoria(){
        int index =0;
        for (int i = 0; i < productocontroller.listaCategoria().size(); i++) {
         listaCategoria[i][0] = productocontroller.listaCategoria().get(i).getId_categoria() + "";
         String data = productocontroller.listaCategoria().get(i).getId_categoria() + " " + productocontroller.listaCategoria().get(i).getNombre_categoria()+ " " + productocontroller.listaCategoria().get(i).getDescripcion();
       listaCategoria[i][1]= data;
       this.cmbCategoria.addItem(data);
       index++;
        }
    }
    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        
        int seleccion =0;
        int seleccion_u =0;
        int seleccion_d=0;
        for (int i = 0; i < listaProveedor.length; i++) {
            if(listaProveedor[i][1].equals(cbProveedor.getSelectedItem().toString())){
                seleccion= Integer.parseInt(listaProveedor[i][0]);
            }
        }
        for (int i = 0; i < listaExistencia.length; i++) {
            if(listaExistencia[i][1].equals(cmbExistencia.getSelectedItem().toString())){
                seleccion_u= Integer.parseInt(listaExistencia[i][0]);
            }
        }
        for (int i = 0; i < listaCategoria.length; i++) {
            if(listaCategoria[i][1].equals(cmbCategoria.getSelectedItem().toString())){
                seleccion_d= Integer.parseInt(listaCategoria[i][0]);
            }
        }
        int codi = Integer.parseInt(txtCodigoPro.getText());
        double pre = Double.parseDouble(txtPrecio.getText());
        Producto producto= new Producto(codi,txtNombre.getText(),pre,seleccion,seleccion_u,seleccion_d
        );
        productocontroller.asignarProducto(producto);
        this.limpiarTexto();
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        int codiPro = Integer.parseInt(txtCodigoPro.getText().trim());
        this.limpiarTexto();
        Producto pro = this.productocontroller.existeProducto(codiPro);
        if(pro != null){
            this.lblIdPro.setText(Integer.toString(pro.getId_producto()));
            this.txtCodigoPro.setText(Integer.toString(pro.getCodigoProducto()));
            this.txtNombre.setText(pro.getNombre());
            this.txtPrecio.setText(Double.toString(pro.getPrecioActual()));
            this.txtNombre.setText(pro.getNombre());
         
        }else{
            JOptionPane.showMessageDialog(this, "No se encontro el producto con el codigo : " + codiPro);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        int codi = Integer.parseInt(txtCodigoPro.getText());
         int seleccion =0;
        int seleccion_u =0;
        int seleccion_d=0;
        for (int i = 0; i < listaProveedor.length; i++) {
            if(listaProveedor[i][1].equals(cbProveedor.getSelectedItem().toString())){
                seleccion= Integer.parseInt(listaProveedor[i][0]);
            }
        }
        for (int i = 0; i < listaExistencia.length; i++) {
            if(listaExistencia[i][1].equals(cmbExistencia.getSelectedItem().toString())){
                seleccion_u= Integer.parseInt(listaExistencia[i][0]);
            }
        }
        for (int i = 0; i < listaCategoria.length; i++) {
            if(listaCategoria[i][1].equals(cmbCategoria.getSelectedItem().toString())){
                seleccion_d= Integer.parseInt(listaCategoria[i][0]);
            }
        }
        double pre = Double.parseDouble(txtPrecio.getText());
        Producto producto= new Producto(txtNombre.getText(),pre,seleccion,seleccion_u,seleccion_d
        );
        producto.setCodigoProducto(codi);
        productocontroller.editarProducto(producto);
        this.limpiarTexto();
    }//GEN-LAST:event_btnEditarActionPerformed


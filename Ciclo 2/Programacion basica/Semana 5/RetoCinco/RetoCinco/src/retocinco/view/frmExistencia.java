    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
        int codi = Integer.parseInt(txtId.getText());
        String nom = txtNombre.getText();
        if(this.existenciaController.actualizarExistencia(codi, nom)== true){
           JOptionPane.showMessageDialog(this, "LA EXISTENCIA SE ACTUALIZO");
        }else{
            JOptionPane.showMessageDialog(this, "LA EXISTENCIA NO SE ACTUALIZO");
            this.limpiarTexto();
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int codigo = Integer.parseInt(txtId.getText());
        boolean seElimino = this.existenciaController.eliminarExistencia(codigo);
        if(seElimino == true){
            JOptionPane.showMessageDialog(this, "SE ELIMINO LA  EXISTENCIA");
        }else{
            JOptionPane.showMessageDialog(this, "NO SE ELIMINO LA  EXISTENCIA");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed


    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        int codi = Integer.parseInt(txtId.getText().trim());
       
        Existencia exis = this.existenciaController.existeExistencia(codi);
        if(exis!=null){
           //this.lblId.setText(Integer.toString(exis.getId_existencia()));
           this.txtNombre.setText(exis.getNombre());
        }else{
            JOptionPane.showMessageDialog(this, "No se encontro la existencia" + codi);
        }
        
    }//GEN-LAST:event_btnBuscarActionPerformed

